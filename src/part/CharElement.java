package part;

public class CharElement {

    private char char1;
    private char char2;

    public CharElement(){

    }

    public CharElement(char char1, char char2){
        this.char1 = char1;
        this.char2 = char2;
    }

    public char getChar1() {
        return char1;
    }

    public void setChar1(char char1) {
        this.char1 = char1;
    }

    public char getChar2() {
        return char2;
    }

    public void setChar2(char char2) {
        this.char2 = char2;
    }
}
